//File Size Constants
const SizeScalingFactor = 1024; //in case we want 1000 instead of 1024

export const B = 1;
export const KB = SizeScalingFactor * B;
export const MB = SizeScalingFactor * KB;
export const GB = SizeScalingFactor * MB;

//Time Constants
export const Second = 1;
export const Minute = 60 * Second;
export const Hour = 60 * Minute;
export const Day = 24 * Hour;
export const Week = 7 * Day;
export const Month = 30 * Day;
export const Year = 365 * Day;

